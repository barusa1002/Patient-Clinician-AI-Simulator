# AI評価プロンプト作成
# ==========================================================

def build_evaluation_prompt(scenario, subscenario, chat_history, prescription_notes=None):

    conversation = ""
    for role, msg in chat_history:
        speaker = "実習生" if role == "user" else "患者・医療者"
        conversation += f"{speaker}：{msg}\n"

    # 評価項目取得（辞書のキーを使用）
    checklist = EVALUATION_CHECKLISTS.get(scenario, {})

    checklist_text = "\n".join(
        f"- {item}" for item in checklist
    )

    # 疑義照会：処方箋備考欄セクション
    prescription_section = ""
    if scenario == "疑義照会":
        if prescription_notes:
            prescription_section = f"""
【処方箋備考欄（学生が記載した内容）】
- 日時：{prescription_notes.get('date_time', '（未記載）')}
- 照会方法：{prescription_notes.get('method', '（未記載）')}
- 照会者：{prescription_notes.get('pharmacist_name', '（未記載）')}
- 照会先：{prescription_notes.get('doctor_name', '（未記載）')}
- 変更内容：{prescription_notes.get('change_content', '（未記載）')}
"""
        else:
            prescription_section = "\n【処方箋備考欄】未記入\n"

    prompt = f"""
あなたは薬学実習の評価者です。
以下の会話を客観的に評価してください。

【シナリオ】
{scenario} / {subscenario}

【会話ログ】
{conversation}{prescription_section}
【評価項目】
{checklist_text}

【評価ルール】

1 = 達成  
0 = 未達成  
null = 会話から判断できない

重要ルール：

・scoresには必ず上記の評価項目のみ含める  
・新しい評価項目を追加しない  
・評価項目を省略しない  

【出力形式】

必ず以下のJSONのみ出力してください。

{{
  "scores": {{
    "評価項目": 1
  }},
  "achieved": [
    "達成できた項目"
  ],
  "missing": [
    {{
      "item": "不足項目",
      "reason": "理由"
    }}
  ],
  "advice": [
    "改善アドバイス"
  ],
  "comment": "総合評価"
}}

JSON以外の文章は絶対に出力しない。
"""

    return prompt


