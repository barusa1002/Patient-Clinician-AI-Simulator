# 評価保存（Supabase版）
# ==========================================================
def save_evaluation(user_id, scenario, subscenario, chat_history, evaluation_text,
                    prescription_notes=None):

    try:
        eval_data = {
            "chat_history": chat_history,
            "result": evaluation_text,
        }
        if prescription_notes:
            eval_data["prescription_notes"] = prescription_notes

        supabase.table("evaluations").insert({
            "user_id": user_id,
            "scenario": scenario,
            "subscenario": subscenario,
            "evaluation": eval_data,
        }).execute()

    except Exception as e:
        print(f"保存エラー: {e}")

# ==========================================================
# 個人評価取得
# ==========================================================

def load_user_evaluations(user_id):

    res = supabase.table("evaluations") \
        .select("*") \
        .eq("user_id", user_id) \
        .order("created_at", desc=True) \
        .execute()

    return res.data


# ==========================================================
# 全学生評価取得（教員用）
# ==========================================================
def load_all_students_evaluations():

    res = supabase.table("evaluations") \
        .select("*") \
        .order("created_at", desc=True) \
        .execute()

    result = {}

    for row in res.data:
        uid = row["user_id"]

        if uid not in result:
            result[uid] = []

        eval_data = row.get("evaluation", {})

        result[uid].append({
            "timestamp": row["created_at"],
            "scenario": row.get("scenario"),
            "subscenario": row.get("subscenario"),
            "chat_history": eval_data.get("chat_history"),
            "evaluation": eval_data.get("result")
        })

    return result

